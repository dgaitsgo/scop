#ifndef __FLAGS_H
# define __FLAGS_H

# define BIT_SET(a,b) ((a) |= (1<<(b)))
# define BIT_CLEAR(a,b) ((a) &= ~(1<<(b)))
# define BIT_FLIP(a,b) ((a) ^= (1<<(b)))
# define BIT_CHECK(a,b) ((a) & (1<<(b)))

#endif
